	/* AppendDeclTrees : append tree2 onto tree1 */
	tTree AppendDeclTrees (tTree tree1, tTree tree2)
	{
		tTree ret = tree2;

		if (tree1->Kind != kNullDecls)
		{
			tTree leaf = tree1;
			ret = tree1;
			while (leaf->Decl.next && leaf->Decl.next->Kind != kNullDecls)
			{ leaf = leaf->Decl.next; }
			leaf->Decl.next = tree2;
		}
		return ret;
	}


